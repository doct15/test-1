from django import template

################################################################################
## Import Third Party Models.                                                 ##
################################################################################
from urllib.parse import quote_plus 					## Social Sharing

################################################################################
## Import Defined Models.                                                     ##
################################################################################







################################################################################
## Define the Urlify Function.                                                ##
################################################################################
## Register the Django Template Library.                                      ##
register = template.Library()

@register.filter
def urlify(value):
    return quote_plus(value)
